SUBROUTINE TRIP_ALLOC(YDTRIP)
!
USE MODD_SURFEX_TRIP_n, ONLY : TRIP_MODEL_t
!
USE MODD_TRIP, ONLY : TRIP_INIT
USE MODD_TRIP_GRID, ONLY : TRIP_GRID_NULLIFY
USE MODD_TRIP_DIAG, ONLY : TRIP_DIAG_NULLIFY
!
USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
!
IMPLICIT NONE
!
TYPE (TRIP_MODEL_t), INTENT (INOUT) :: YDTRIP
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK("TRIP_ALLOC",0,ZHOOK_HANDLE)
!
CALL TRIP_INIT(YDTRIP%TP)
CALL TRIP_GRID_NULLIFY(YDTRIP%TPG)
CALL TRIP_DIAG_NULLIFY(YDTRIP%TPDG)
!
IF (LHOOK) CALL DR_HOOK("TRIP_ALLOC",1,ZHOOK_HANDLE)
!
END SUBROUTINE TRIP_ALLOC
